#string
x = "hello world"
print(type(x))

#int
x = 10
print(type(x))

#float
x = 10.5
print(type(x))

#complex
x = 1j
print(type(x))

#list
x = ["apple", "banana", "cherry"]
print(type(x))

#tuple
x = ("apple", "banana", "cherry")
print(type(x))

#range
x = range(6)
print(type(x))

#dict
x = {"name" : "John", "age" : 36}
print(type(x))

#set
x = {"apple", "banana", "cherry"}
print(type(x))

#frozenset
x = frozenset({"apple", "banana", "cherry"})
print(type(x))

#boolean
x = True
print(type(x))

#none
x = None
print(type(x))